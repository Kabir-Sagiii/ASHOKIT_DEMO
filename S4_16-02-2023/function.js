function fnAdd(x, y) {
  // Function Parameters
  var num1 = x; //Variable Initialization
  var num2 = y;
  var result; //Variable Declaration

  result = num1 + num2;
  console.log(result);
}

fnAdd(5, 5); // 10  // 5 + 5  // Functions Arguments

fnAdd(10, 10); //20 // 10 + 10

fnAdd(50, 50); //100
fnAdd(20, 70);
fnAdd(4, 3);
